<?php

class ClaseEjemplo
{
    /**
     * Realiza una suma de dos números.
     *
     * @param int|float $num1 El primer número.
     * @param int|float $num2 El segundo número.
     * @return int|float El resultado de la suma.
     */
    public function suma($num1, $num2)
    {
        return $num1 + $num2;
    }
}
